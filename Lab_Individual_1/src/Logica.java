	public void clickear(int pmouseX, int pmouseY) {
		// Interaccion para cambiar de pantalla
		if ( pantalla == 0) { 
		    if (app.mouseX >=406 && app.mouseX <=790 && app.mouseY >=290 && app.mouseY <=461) {
		      pantalla = 1;
		    }
		  }
		  if ( pantalla == 0) { 
		    if (app.mouseX >=440 && app.mouseX <=760 && app.mouseY >=498 && app.mouseY <=605) {
		      pantalla = 2;
		    }
		  }
		  if ( pantalla == 2) { 
		    if (app.mouseX >=950 && app.mouseX <=1178 && app.mouseY >=600 && app.mouseY <=660) {
		      pantalla = 0;
		    }
		  }
		  if ( pantalla == 1) { 
		    if (app.mouseX >=100 && app.mouseX <=200 && app.mouseY >=100 && app.mouseY <=200) {
		      pantalla = 4;
		    }
		  }
	}
	
